  window.generateAsciiArt = function() {
    const inputText = document.getElementById("inputText").value;
    const prefix = document.getElementById("prefix").value || "#";
    const suffix = document.getElementById("suffix").value || "";
    const outputArea = document.getElementById("outputArt");
  
    if (!inputText.trim()) {
      outputArea.value = "";
      return;
    }
  
    const lines = Array(6).fill("").map(() => "");
  
    for (const char of inputText) {
      const charLines = ASCII_CHARS[char] || ASCII_CHARS[' '];
  
      for (let i = 0; i < 6; i++) {
        lines[i] += charLines[i] + " ";
      }
    }
  
    outputArea.value = lines.map(line => prefix + line + suffix).join("\n");
    console.log("Has successfully generate the ASCII art");
  };
  
  window.copyToClipboard = function() {
    const outputArea = document.getElementById("outputArt");
    const asciiText = outputArea.value;
  
    if (asciiText) {
      navigator.clipboard.writeText(asciiText).then(() => {
        console.log("Copied to clipboard!");
      }).catch(err => {
        console.log("Failed to copy text: " + err);
      });
    }
  };

